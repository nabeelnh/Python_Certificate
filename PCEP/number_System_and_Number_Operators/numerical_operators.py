#!/usr/local/bin/python3.8

print ( 2 + 2 )  # addition

print ( 10 - 2 )  # subtraction

print ( 5 * 5 )  # multipication

print ( 10 / 2 )  # division, converts results to a float even when evenly divided

print ( 10 // 2 ) # floor divison, returns number without the reminder as an int

print ( 10 % 2 ) # Modulo division, returns the reminder as an int

print ( 5 ** 2 )  # exponent / power of (e.g. 5^2)

print ( 5e2 )  # Euler's number (e.g. 5 * (10 ** 2))
